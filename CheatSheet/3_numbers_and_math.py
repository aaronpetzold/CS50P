# === Numbers and Math ===

# Types
x = 10      # int
y = 3.14    # float
z = 2 + 3j  # complex

# Conversions
int("42")      # 42
float("3.14")  # 3.14
str(100)       # "100"

# Arithmetic
a = 10 + 5     # 15 (addition)
b = 10 - 5     # 5 (subtraction)
c = 10 * 5     # 50 (multiplication)
d = 10 / 3     # 3.333... (true division)
e = 10 // 3    # 3 (floor division)
f = 10 % 3     # 1 (modulo/remainder)
g = 10 ** 2    # 100 (exponent)

# Math functions
import math
math.sqrt(25)      # 5.0
math.pow(2, 3)     # 8.0
abs(-10)           # 10 (absolute)
round(3.14159, 2)  # 3.14